// Составить программу: определения наибольшего из двух чисел а и b.

public class Task06 {

	public static void main(String[] args) {

		int a = 1;
		int b = 2;

		if (a > b) {

			System.out.println("a больше b");

		} else if (b > a) {

			System.out.println("b больше a");

		} else {

			System.out.println("ответ неверный");

		}

	}

}
