package by.htp.branching01.main;

// Подсчитать количество отрицательных среди чисел а, b, с

public class Task18 {
	
    public static void main(String[] args) {
    	
    	int a = 1;
    	int b = 2;
    	int c = -3;
    	int i = 0;
    	
    	
    	if(a < 0) {
    		
    		i++;
    		}
    	if(b < 0) {
    		
    		i++;
    		
    		}
    	if(c < 0){
    		
    		i++;
    		
    	}
    	
    	System.out.println(i);
    	
    }

}
