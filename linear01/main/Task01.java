package by.htp.linear01.main;

// Даны два действительных числа x и y. 
// Вычислить их сумму, разность, произведение и частное.

public class Task01 {
	
	public static void main(String[] args) {
		
		int x = 20;
		int y = 2;
		
		int z1 = x + y;
		int z2 = x - y;
		int z3 = x * y;
		int z4 = x / y;
		
		System.out.println("x + y = " + z1);
		System.out.println("x - y = " + z2);
		System.out.println("x * y = " + z3);
		System.out.println("x / y = " + z4);
		
	}

}
