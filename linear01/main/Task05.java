package by.htp.linear01.main;

// Составить алгоритм нахождения среднего арифметического двух чисел

public class Task05 {
	
	public static void main(String[] args) {
		
		int x;
		int y;
		int z;
		
		x = 78;
		y = 93;
		
		z = (x + y) / 2;
				
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println("(x + y) / 2 = " + z);
	}
	
	

}
